void EinkCanvas::Clear(bool white)
{
    memset(buf_, white ? 0xFF : 0x00, BUF_SIZE);
    dirty_ = true;
    dirtyX0_ = 0; dirtyY0_ = 0;
    dirtyX1_ = WIDTH; dirtyY1_ = HEIGHT;
}

void EinkCanvas::MarkDirty(int16_t x, int16_t y)
{
    dirty_ = true;
    if (x < (int16_t)dirtyX0_) dirtyX0_ = x;
    if (y < (int16_t)dirtyY0_) dirtyY0_ = y;
    if (x + 1 > (int16_t)dirtyX1_) dirtyX1_ = x + 1;
    if (y + 1 > (int16_t)dirtyY1_) dirtyY1_ = y + 1;
}

void EinkCanvas::MarkDirtyRect(int16_t x, int16_t y, uint16_t w, uint16_t h)
{
    dirty_ = true;
    if (x < (int16_t)dirtyX0_) dirtyX0_ = x < 0 ? 0 : x;
    if (y < (int16_t)dirtyY0_) dirtyY0_ = y < 0 ? 0 : y;
    if (x + (int16_t)w > (int16_t)dirtyX1_) dirtyX1_ = x + w > WIDTH ? WIDTH : x + w;
    if (y + (int16_t)h > (int16_t)dirtyY1_) dirtyY1_ = y + h > HEIGHT ? HEIGHT : y + h;
}

void EinkCanvas::SetPixel(int16_t x, int16_t y, bool black)
{
    if (x < 0 || x >= (int16_t)WIDTH || y < 0 || y >= (int16_t)HEIGHT) return;
    uint32_t byte_idx = (y * WIDTH + x) / 8;
    uint8_t bit_mask = 0x80 >> (x % 8);
    if (black)
        buf_[byte_idx] &= ~bit_mask;
    else
        buf_[byte_idx] |= bit_mask;
    MarkDirty(x, y);
}

bool EinkCanvas::GetPixel(int16_t x, int16_t y) const
{
    if (x < 0 || x >= (int16_t)WIDTH || y < 0 || y >= (int16_t)HEIGHT) return false;
    uint32_t byte_idx = (y * WIDTH + x) / 8;
    uint8_t bit_mask = 0x80 >> (x % 8);
    return (buf_[byte_idx] & bit_mask) == 0;
}

void EinkCanvas::DrawHLine(int16_t x, int16_t y, uint16_t w, bool black)
{
    for (uint16_t i = 0; i < w; i++) SetPixel(x + i, y, black);
}

void EinkCanvas::DrawVLine(int16_t x, int16_t y, uint16_t h, bool black)
{
    for (uint16_t i = 0; i < h; i++) SetPixel(x, y + i, black);
}

void EinkCanvas::DrawLine(int16_t x0, int16_t y0, int16_t x1, int16_t y1, bool black)
{
    int16_t dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int16_t dy = -abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int16_t err = dx + dy;
    for (;;) {
        SetPixel(x0, y0, black);
        if (x0 == x1 && y0 == y1) break;
        int16_t e2 = 2 * err;
        if (e2 >= dy) { err += dy; x0 += sx; }
        if (e2 <= dx) { err += dx; y0 += sy; }
    }
}

void EinkCanvas::DrawRect(int16_t x, int16_t y, uint16_t w, uint16_t h, bool fill, bool black)
{
    if (fill) {
        for (uint16_t j = 0; j < h; j++)
            DrawHLine(x, y + j, w, black);
    } else {
        DrawHLine(x, y, w, black);
        DrawHLine(x, y + h - 1, w, black);
        DrawVLine(x, y, h, black);
        DrawVLine(x + w - 1, y, h, black);
    }
    MarkDirtyRect(x, y, w, h);
}

void EinkCanvas::DrawProgressBar(int16_t x, int16_t y, uint16_t w, uint16_t h, uint8_t percent)
{
    if (percent > 100) percent = 100;
    DrawRect(x, y, w, h, false, true);
    uint16_t fillW = (uint16_t)((uint32_t)(w - 2) * percent / 100);
    if (fillW > 0)
        DrawRect(x + 1, y + 1, fillW, h - 2, true, true);
}

void EinkCanvas::DrawBitmap(int16_t x, int16_t y, uint16_t w, uint16_t h, const uint8_t* data)
{
    uint16_t bytesPerRow = w / 8;
    for (uint16_t j = 0; j < h; j++) {
        for (uint16_t i = 0; i < w; i++) {
            uint8_t byte = data[j * bytesPerRow + i / 8];
            bool black = (byte & (0x80 >> (i % 8))) != 0;
            SetPixel(x + i, y + j, black);
        }
    }
    MarkDirtyRect(x, y, w, h);
}

uint16_t EinkCanvas::DrawChar(int16_t x, int16_t y, char c, bool black)
{
    if (c < 32 || c > 126) c = '?';
    uint8_t idx = c - 32;
    for (uint8_t col = 0; col < 5; col++) {
        uint8_t line = FONT_5X7[idx][col];
        for (uint8_t row = 0; row < 7; row++) {
            if (line & (1 << row))
                SetPixel(x + col, y + row, black);
        }
    }
    MarkDirtyRect(x, y, 5, 7);
    return 6;
}

uint16_t EinkCanvas::DrawText(int16_t x, int16_t y, const char* text, bool black)
{
    uint16_t advance = 0;
    while (*text) {
        advance += DrawChar(x + advance, y, *text, black);
        text++;
    }
    return advance;
}

uint16_t EinkCanvas::DrawNumber(int16_t x, int16_t y, int32_t num, bool black)
{
    char buf[12];
    int len = 0;
    if (num < 0) { buf[len++] = '-'; num = -num; }
    if (num == 0) { buf[len++] = '0'; }
    else {
        char tmp[11]; int tl = 0;
        while (num > 0) { tmp[tl++] = '0' + (num % 10); num /= 10; }
        for (int i = tl - 1; i >= 0; i--) buf[len++] = tmp[i];
    }
    buf[len] = '\0';
    return DrawText(x, y, buf, black);
}

void EinkCanvas::DrawLargeDigit(int16_t x, int16_t y, uint8_t digit, bool black)
{
    if (digit > 9) return;
    char c = '0' + digit;
    // 2x scale of 5x7 font -> 10x14
    if (c < 32 || c > 126) return;
    uint8_t idx = c - 32;
    for (uint8_t col = 0; col < 5; col++) {
        uint8_t line = FONT_5X7[idx][col];
        for (uint8_t row = 0; row < 7; row++) {
            if (line & (1 << row)) {
                SetPixel(x + col * 2, y + row * 2, black);
                SetPixel(x + col * 2 + 1, y + row * 2, black);
                SetPixel(x + col * 2, y + row * 2 + 1, black);
                SetPixel(x + col * 2 + 1, y + row * 2 + 1, black);
            }
        }
    }
    MarkDirtyRect(x, y, 10, 14);
}

uint16_t EinkCanvas::DrawLargeNumber(int16_t x, int16_t y, int32_t num, bool black)
{
    char buf[12]; int len = 0;
    if (num < 0) { DrawChar(x, y + 3, '-', black); x += 8; len++; num = -num; }
    if (num == 0) { DrawLargeDigit(x, y, 0, black); return 12; }
    char tmp[11]; int tl = 0;
    while (num > 0) { tmp[tl++] = num % 10; num /= 10; }
    uint16_t advance = 0;
    for (int i = tl - 1; i >= 0; i--) {
        DrawLargeDigit(x + advance, y, tmp[i], black);
        advance += 12;
    }
    return advance;
}

void EinkCanvas::GetDirtyRegion(uint16_t& x, uint16_t& y, uint16_t& w, uint16_t& h) const
{
    x = dirtyX0_; y = dirtyY0_;
    w = dirtyX1_ > dirtyX0_ ? dirtyX1_ - dirtyX0_ : 0;
    h = dirtyY1_ > dirtyY0_ ? dirtyY1_ - dirtyY0_ : 0;
}
